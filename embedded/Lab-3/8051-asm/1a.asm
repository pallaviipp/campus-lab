ORG 00H

MAIN:
SETB P2.0 
LCALL DELAY_3MS
CLR P2.0
LCALL DELAY_6MS
LCALL DELAY_6MS
SJMP MAIN

DELAY_3MS:
MOV TMOD, #00H
MOV TH0, #0AFH ;calculated register value is A998 
MOV TL0, #068H
SETB TR0
AGAIN_3MS: JNB TF0, AGAIN_3MS
	CLR TR0
	CLR TF0
RET

DELAY_6MS:
MOV TMOD, #00H
MOV TH0, #05AH ;calculated register value is A998 
MOV TL0, #030H
SETB TR0
AGAIN_6MS: JNB TF0, AGAIN_6MS
	CLR TR0
	CLR TF0
RET


END