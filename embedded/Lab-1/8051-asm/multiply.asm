ORG 00H
	
MOV 22H, #0FFH
MOV 15H, #0DEH

MOV R0, 22H
MOV R1, 15H

MOV R3, #0H
MOV R4, #0H

CLR C
MOV A, #00H

ADDING:
	MOV A, R3
	ADD A, R0
	MOV R3, A
	JNC NO_CARRY
	
	INC R4
	
	NO_CARRY:
	DJNZ R1, ADDING

MOV 19H, R3
MOV 1AH, R4

DISPLAY: 
	MOV P0, 19H
	LCALL DELAY
	LCALL DELAY
	MOV P0, 1AH
	LCALL DELAY
	LCALL DELAY
	SJMP DISPLAY

DELAY:
	MOV R0, #0ffh
	OUTER:
	MOV R1, #0ffh
	INNER:
	DJNZ R1, INNER
	DJNZ R0, OUTER
	RET

END 